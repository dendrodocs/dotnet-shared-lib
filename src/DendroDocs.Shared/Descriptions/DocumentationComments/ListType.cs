namespace DendroDocs.DocumentationComments;

/// <summary>
/// Provides constant values for XML documentation comment list type attribute values.
/// </summary>
public class ListType
{
    /// <summary>
    /// The "bullet" list type constant for bullet lists.
    /// </summary>
    public const string Bullet = "bullet";
    /// <summary>
    /// The "number" list type constant for numbered lists.
    /// </summary>
    public const string Number = "number";
    /// <summary>
    /// The "definition" list type constant for definition lists.
    /// </summary>
    public const string Definition = "definition";
    /// <summary>
    /// The "table" list type constant for table lists.
    /// </summary>
    public const string Table = "table";
}
